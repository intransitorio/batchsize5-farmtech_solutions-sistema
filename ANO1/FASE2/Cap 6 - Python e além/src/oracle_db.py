def inserir_colheita(conexao, colheita: dict[str, Any]) -> None:
    """Insere uma colheita no Oracle preservando o ID da memória."""
    cursor = conexao.cursor()
    try:
        cursor.execute(
            """
            INSERT INTO colheitas (
                id, id_talhao, data_colheita, modo, producao_ton,
                perda_percentual, perda_toneladas, umidade_pct, brix, meses_plantio
            ) VALUES (
                :id, :id_talhao, TO_DATE(:data_colheita, 'YYYY-MM-DD'), :modo, :producao_ton,
                :perda_percentual, :perda_toneladas, :umidade_pct, :brix, :meses_plantio
            )
            """,
            id=colheita["id"],
            id_talhao=colheita["id_talhao"],
            data_colheita=colheita["data_colheita"],
            modo=colheita["modo"],
            producao_ton=colheita["producao_ton"],
            perda_percentual=colheita["perda_percentual"],
            perda_toneladas=colheita["perda_toneladas"],
            umidade_pct=colheita["umidade_pct"],
            brix=colheita["brix"],
            meses_plantio=colheita["meses_plantio"],
        )
        conexao.commit()
    except Exception:
        conexao.rollback()
        raise
    finally:
        cursor.close()



def listar_colheitas_db(conexao) -> list[dict[str, Any]]:
    """Lista todas as colheitas do Oracle com o nome do talhão associado."""
    cursor = conexao.cursor()
    try:
        cursor.execute(
            """
            SELECT c.id,
                   c.id_talhao,
                   t.nome AS nome_talhao,
                   t.area_ha,
                   TO_CHAR(c.data_colheita, 'YYYY-MM-DD') AS data_colheita,
                   c.modo,
                   c.producao_ton,
                   c.perda_percentual,
                   c.perda_toneladas,
                   c.umidade_pct,
                   c.brix,
                   c.meses_plantio
              FROM colheitas c
              JOIN talhoes t ON t.id = c.id_talhao
          ORDER BY c.id
            """
        )
        registros = []
        for linha in cursor.fetchall():
            registros.append(
                {
                    "id": int(linha[0]),
                    "id_talhao": int(linha[1]),
                    "nome_talhao": linha[2],
                    "area_ha": float(linha[3]),
                    "data_colheita": linha[4],
                    "modo": linha[5],
                    "producao_ton": float(linha[6]),
                    "perda_percentual": float(linha[7]),
                    "perda_toneladas": float(linha[8]),
                    "umidade_pct": float(linha[9]),
                    "brix": float(linha[10]),
                    "meses_plantio": int(linha[11]),
                }
            )
        return registros
    finally:
        cursor.close()



