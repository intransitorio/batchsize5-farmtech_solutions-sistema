# -----------------------------------------------------------------------------
# Sincronização
# -----------------------------------------------------------------------------


def sincronizar_memoria_para_oracle(conexao, talhoes: list[dict[str, Any]], colheitas: list[dict[str, Any]]) -> dict[str, int]:
    """Insere no Oracle os registros da memória que ainda não existem no banco."""
    ids_talhoes_db = {registro["id"] for registro in listar_talhoes_db(conexao)}
    ids_colheitas_db = {registro["id"] for registro in listar_colheitas_db(conexao)}

    inseridos_talhoes = 0
    inseridos_colheitas = 0

    for talhao in talhoes:
        if talhao["id"] not in ids_talhoes_db:
            inserir_talhao(conexao, talhao)
            inseridos_talhoes += 1

    for colheita in colheitas:
        if colheita["id"] not in ids_colheitas_db:
            inserir_colheita(conexao, colheita)
            inseridos_colheitas += 1

    return {
        "talhoes_inseridos": inseridos_talhoes,
        "colheitas_inseridas": inseridos_colheitas,
    }



def carregar_oracle_para_memoria(conexao) -> dict[str, list[dict[str, Any]]]:
    """Lê talhões e colheitas do Oracle e devolve em formato compatível com a aplicação."""
    return {
        "talhoes": listar_talhoes_db(conexao),
        "colheitas": listar_colheitas_db(conexao),
    }
